<h3>معلومات الهوية الشخصية</h3>

<p>تقوم Enginx Solutions بجمع معلومات عن الهوية الشخصية للمستخدمين بطرق مختلفة، وتشمل ولا تقتصر فقط على: أوقات زيارة المستخدم للموقع، التسجيل في موقع Enginx Solutions، ملء الطلب، نموذج تعبئة الإستبيان، وغير ذلك من الأنشطة، الخدمات، والميزات أو المصادر التي قد نوفرها على خدمتنا على الموقع الإلكترونى. كما أنه قد يُطلب من المستخدمين توضيح الاسم بشكل كامل، عنوان البريد الإلكتروني، رقم الهاتف، وبيانات العنوان والنشاط التجارى.</p>

<h3>استخدام الموقع الالكترونى</h3>

<p>يستخدم موقعنا الإلكتروني ملفات تعريف الارتباط (وهي جزء صغير من المعلومات التي يرسلها خادم الويب لتخزين المعلومات على متصفح الويب الخاص بك بحيث يمكن قراءتها لاحقًا من ذلك المتصفح) لجمع معلومات حول استخدامك لموقعنا وتسهيل زيارة الزيارات. قد تقوم ملفات تعريف الارتباط الموجودة على موقعنا الإلكتروني بتجميع معلومات حول نطاق الإنترنت وعنوان بروتوكول الإنترنت الذي يمكنك من خلاله الوصول إلى موقعنا الإلكتروني والتاريخ والوقت اللذين قمت بزيارتهما على موقعنا الإلكتروني والصفحات التي زرتها على موقعنا. يمكنك تحديد استخدام ملفات تعريف الارتباط بشكل مستقل من خلال إعدادات متصفح الويب الخاص بك ، ولكن هذا قد يحد من وصولك إلى موقعنا أو استخدامه. يتم استخدام المعلومات التي نجمعها من ملفات تعريف الارتباط والمعلومات التي تزودنا بها عند طلب معلومات منا عبر موقعنا الإلكتروني أو الاستبيانات أو الاستبيانات الكاملة المنشورة على موقعنا لتزويدك بالمعلومات التي طلبتها، للحفاظ على الاطلاع على منتجاتنا وخدماتنا الحالية والجديدة (ما لم تكن قد ذكرت أنك لا ترغب في تلقي مواد تسويقية منا) ، لتحسين وظائف موقعنا ، لتسهيل استخدامك لموقعنا، لإنشاء بيانات إحصائية لتسويقنا الأهداف، لتطوير ملفات تعريف غير شخصية من الأطراف التي تهتم بمنتجاتنا وخدماتنا، لبناء قواعد بيانات للمواد التسويقية مثل البريد الإلكتروني والحملات ودعم العميل.</p>

<h3>معلومات لا تتعلق بالهوية الشخصية</h3>

<p>تقوم Enginx Solutions بجمع بعض المعلومات عن المستخدمين أينما تفاعلوا مع كافة البرامج والخدمات، وهذه المعلومات لا تتعلق بالهوية الشخصية إنما قد تتضمن معلومات تقنية عن أساليب اتصال المستخدم بكافة خدماتنا وبرامجنا، مثل الشركة المستخدمة لتقديم خدمة الإنترنت وغيرها من المعلومات المشابهة.</p>

<h3>كيف نقوم باستخدام المعلومات التي تم جمعها</h3>

<p>قوم Enginx Solutions بجمع واستخدام المعلومات الشخصية للمستخدمين في الأغراض التالية: تحسين جودة خدمة العملاء: تساعدنا المعلومات الخاصة بك على سهولة الاستجابة لطلباتك الموجهة لخدمة العملاء واحتياجاتك للدعم بشكل أكثر فاعلية. لتحسين كافة البرامج والخدمات: نسعى باستمرار لتحسين العروض المقدمة من خلال برامجنا وخدماتنا وفقاً للمعلومات والملاحظات التي نتلقاها منك من أجل فهم أفضل لمصالح واحتياجات مستخدمي خدمتنا. إرسال رسائل إلكترونية بشكل دوري: عنوان البريد الإلكتروني الذي يسجله المستخدمون للتعامل مع طلبهم، سوف يستخدم فقط في إرسال معلومات وتحديثات لهم تتعلق بطلبهم. وقد يُستخدم أيضاً في الاستجابة على الاستفسارات، أو غيرها من الطلبات والأسئلة. في حال قرر المستخدم الانضمام إلى قائمة مراسلاتنا، فإنه سوف يتلقى رسائل على بريده الإلكتروني قد تتضمن أخبار Enginx Solutions، آخر التحديثات، ومعلومات تتعلق بالخدمات والعروض الجديدة. وإذا أراد المستخدم في أي وقت إلغاء خاصية تلقي أي رسائل مستقبلية، فإننا نقدم له توجيهات مفصلة حول إلغاء التسجيل في نهاية كل رسالة على بريده الإلكتروني، او يمكن للمستخدم التواصل معنا من خلال أرقام الهواتف الخاصة بنا أو موقعنا الإلكتروني.</p>

<h3>كيف نقوم بحماية معلوماتك</h3>

<p>نحن نتبع الإجراءات السليمة ومعايير الأمان في جمع وحفظ والتعامل مع البيانات، وذلك لحماية تلك البيانات ضد التعامل غير المصرح به، أو التعديل، أو الإفصاح أو إتلاف بياناتك الشخصية، واسم المستخدم، وكلمة المرور، ومعلومات تخص تعاملاتك والبيانات المخزنة على خدماتنا وبرامجنا. يتم تبادل البيانات الحساسة والخاصة بين Enginx Solutions ومستخدميه من خلال قنوات اتصال مؤمنة ويتم تشفيرها وحمايتها من خلال طرق وأساليب رقمية معتمدة.</p>

<h3>مشاركة البيانات الشخصية</h3>

<p>حن في Enginx Solutions لا نقوم ببيع، أو المتاجرة، أو تأجير بيانات الهوية الشخصية الخاصة بالمستخدمين إلى جهات أخرى، وقد نشارك المعلومات والمستخدمين مع شركائنا، والشركات التابعة والمعلنين وذلك كونك أحد مستخدمي خدماتنا. قد نستخدم طرف ثالث مقدم للخدمة ليساعدنا في تسيير أعمالنا وأيضاً في عمل الخدمة أو في إدارة الأنشطة بالنيابة عنا، مثل الدراسات الاستقصائية. وقد تتم مشاركة بياناتك مع هذا الطرف الثالث في حدود تلك الأغراض المحددة. لن نستخدم معلوماتك الشخصية لأي غرض ، بخلاف ما ورد في سياسة الخصوصية هذه ، دون موافقتك الصريحة. قد نقوم بمعالجة معلوماتك على خادم يقع خارج البلد الذي توجد فيه. إذا تغيرت أي من المعلومات التي قدمتها لنا عبر موقعنا الإلكتروني ، فأنت بحاجة إلى تقديم المشورة إلينا في أقرب وقت ممكن حتى نتمكن من تحديث سجلاتنا.</p>

